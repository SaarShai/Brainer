#!/usr/bin/env python3
"""Optional Kimi K3 adversarial reviewer for redacted, subjective evidence.

Never imported or invoked by the campaign. Credentials, endpoint, and model are
environment-only and are never written to results.
"""
from __future__ import annotations

import argparse
import hashlib
import json
import os
import re
import time
import urllib.request
from pathlib import Path
from typing import Callable

from ab_harness import atomic_json

FORBIDDEN_POST_KEYS = {"deterministic_task_pass", "material_scope_violation", "unrequested_writes"}
SECRET_KEY_RE = re.compile(r"(?i)(api.?key|token|secret|password|credential|private.?key)")
SECRET_VALUE_RE = re.compile(r"(?i)(?:sk|key|token)-[A-Za-z0-9_-]{12,}")


def scrub(value, *, post_run: bool = False):
    if isinstance(value, dict):
        out = {}
        for key, child in value.items():
            if SECRET_KEY_RE.search(str(key)) or (post_run and key in FORBIDDEN_POST_KEYS):
                out[key] = "[REDACTED]"
            else:
                out[key] = scrub(child, post_run=post_run)
        return out
    if isinstance(value, list):
        return [scrub(x, post_run=post_run) for x in value]
    if isinstance(value, str):
        return SECRET_VALUE_RE.sub("[REDACTED]", value)
    return value


def default_transport(url: str, headers: dict, payload: dict, timeout: int) -> dict:
    request = urllib.request.Request(url, data=json.dumps(payload).encode(), headers=headers)
    with urllib.request.urlopen(request, timeout=timeout) as response:
        return json.loads(response.read())


def review(role: str, source: dict, *, model: str, endpoint: str, api_key: str,
           transport: Callable = default_transport, timeout: int = 300) -> dict:
    if role == "blind-subjective":
        generator = str(source.get("generator_model", ""))
        if not generator:
            raise ValueError("blind-subjective review requires generator_model")
        if generator.lower() == model.lower():
            raise ValueError("reviewer must not review output generated by the same model")
    cleaned = scrub(source, post_run=role == "blind-subjective")
    source_bytes = json.dumps(cleaned, sort_keys=True, separators=(",", ":")).encode()
    instruction = {
        "architecture": "Adversarially review this redacted evaluation architecture before launch. Identify validity blockers only.",
        "blind-subjective": ("Blindly review only the supplied subjective criteria. Do not judge or restate deterministic pass, "
                             "scope, or write-boundary gates. Return compact JSON with findings and confidence."),
    }[role]
    prompt = instruction + "\n\nREDACTED INPUT SHA256: " + hashlib.sha256(source_bytes).hexdigest() + "\n" + source_bytes.decode()
    payload = {"model": model, "messages": [{"role": "user", "content": prompt}],
               "response_format": {"type": "json_object"}}
    start = time.monotonic()
    try:
        response = transport(endpoint.rstrip("/") + "/chat/completions",
                             {"Authorization": f"Bearer {api_key}", "Content-Type": "application/json"},
                             payload, timeout)
    except Exception as exc:
        return {"schema_version": 1, "role": role, "reviewer_model_requested": model,
                "review_status": "blocked_transport", "blocker_type": type(exc).__name__,
                "source_redacted_sha256": hashlib.sha256(source_bytes).hexdigest(),
                "request_prompt_sha256": hashlib.sha256(prompt.encode()).hexdigest(),
                "response_sha256": None, "latency_seconds": time.monotonic() - start,
                "usage": {}, "review": None, "credentials_stored": False}
    latency = time.monotonic() - start
    choice = response.get("choices", [{}])[0]
    message = choice.get("message", {})
    content = message.get("content", "") or ""
    reasoning = message.get("reasoning_content", "") or ""
    if not content.strip():
        parsed = None
        review_status = "blocked_empty_content_after_reasoning" if reasoning else "blocked_empty_content"
    else:
        try:
            parsed = scrub(json.loads(content), post_run=False)
            review_status = "complete"
        except json.JSONDecodeError:
            parsed = {"unparsed_response": scrub(content)[:4000]}
            review_status = "incomplete_unparsed"
    response_bytes = json.dumps(response, sort_keys=True, separators=(",", ":")).encode()
    return {"schema_version": 1, "role": role, "reviewer_model_requested": model,
            "reviewer_model_returned": response.get("model"),
            "reviewer_version_returned": response.get("system_fingerprint"),
            "source_redacted_sha256": hashlib.sha256(source_bytes).hexdigest(),
            "request_prompt_sha256": hashlib.sha256(prompt.encode()).hexdigest(),
            "response_sha256": hashlib.sha256(response_bytes).hexdigest(),
            "finish_reason": choice.get("finish_reason"), "review_status": review_status,
            "reasoning_content_present": bool(reasoning),
            "reasoning_content_sha256": hashlib.sha256(reasoning.encode()).hexdigest() if reasoning else None,
            "latency_seconds": latency, "usage": scrub(response.get("usage", {})),
            "review": parsed, "credentials_stored": False,
            "deterministic_gates_reviewed": False if role == "blind-subjective" else None}


def main() -> int:
    ap = argparse.ArgumentParser()
    ap.add_argument("--execute", action="store_true")
    ap.add_argument("--role", choices=["architecture", "blind-subjective"], required=True)
    ap.add_argument("--input", type=Path, required=True)
    ap.add_argument("--out", type=Path, required=True)
    ap.add_argument("--timeout", type=int, default=300)
    args = ap.parse_args()
    if not args.execute:
        raise SystemExit("reviewer is optional and refuses network access without --execute")
    required = {name: os.environ.get(name) for name in
                ("MOONSHOT_API_KEY", "MOONSHOT_BASE_URL", "MOONSHOT_MODEL")}
    missing = [name for name, value in required.items() if not value]
    if missing:
        raise SystemExit(f"missing environment-only reviewer config: {', '.join(missing)}")
    if not 30 <= args.timeout <= 600:
        raise SystemExit("--timeout must be between 30 and 600 seconds")
    source = json.loads(args.input.read_text())
    report = review(args.role, source, model=required["MOONSHOT_MODEL"],
                    endpoint=required["MOONSHOT_BASE_URL"], api_key=required["MOONSHOT_API_KEY"],
                    timeout=args.timeout)
    atomic_json(args.out, report)
    return 0 if report["review_status"] == "complete" else 4


if __name__ == "__main__":
    raise SystemExit(main())
